class User {
  final String uid;
  final String email;
  User({ this.uid, this.email });
}

class UserData{
  final String uid;
  final String email;
  final String username;
  final String idNo;
  final String contact;
  final String profileUrl;

  UserData({this.uid, this.email, this.username, this.idNo, this.contact, this.profileUrl});
}